#Operadores Aritméticos
x = 10
y = 5
#Suma (+)
print("x + y =", x+y)

#Resta (-)
print("x - y =", x-y)

#Multiplicación (*)
print("x * y =", x*y)

#División (/)
print("x / y = ", x/y)

#Módulo (%)
print("x % y =", x%y)

#Exponente (**)
print("x ** y =", x**y)

#División de piso (//)
print("x // y =", x//y)