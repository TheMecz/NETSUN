#Operadores de Relación
#Numéricos
x = 35
y = 30
print("Operadores de relación numéricos: ")
#Iguales (==)
print("x == y es", x==y)
#Diferentes (!=)
print("x != y es", x!=y)
#Mayor (<)
print("x < y es", x<y)
#Menor (>)
print("x > y es", x>y)
#Mayot Igual (<=)
print("x <= y es", x<=y)
#Menor Igual (>=)
print("x >= y es", x>=y)

